                                        <h3>TOEFL Latest Updates 2024</h3>
                                        <p>
                                            15 May 2024: The TOEFL test has introduced a new format that may make things easier for test-takers from India! The Reading section has been revamped, now featuring just 2 passages with 10 questions each, down from the previous 3-4 passages. This means less reading and less time spent answering questions. You’ll now only need 35 minutes for the Reading section, compared to the previous 54-72 minutes. While the TOEFL continues to assess your English skills for studying abroad, this change allows you to focus on a smaller amount of material during your preparation.
                                        </p>
