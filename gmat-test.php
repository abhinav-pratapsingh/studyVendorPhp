                                            <li>The GMAT Focus will assess the applicant's ability to read and interpret data in multiple formats, including tables and graphs.
                                            </li>
                                            <li>The GMAT exam pattern for this section is designed to reflect how today's business world relies on data. It simulates how businesses and managers gather data from various sources, identify patterns, and use that information to make decisions and solve both small and large business problems.</li>
