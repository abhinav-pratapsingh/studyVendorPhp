                                    <div class="mt-4">
                                        <p>
                                            The GRE (Graduate Record Examination) is a standardized test used for admissions to graduate programs, primarily in the United States. It assesses verbal reasoning, quantitative reasoning, and analytical writing skills. The test consists of three main sections: Analytical Writing (two tasks), Verbal Reasoning (text completion, sentence equivalence, and reading comprehension), and Quantitative Reasoning (basic arithmetic, algebra, geometry, and data analysis). Scores are used by graduate schools to evaluate applicants' preparedness for advanced study. The GRE is offered year-round and can be taken multiple times, with results valid for five years.
                                        </p>
                                    </div>

