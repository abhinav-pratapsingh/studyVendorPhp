                                    <div class="mt-4">
                                        <p>
                                            The SAT 2024 exam consists of two main sections: Evidence-Based Reading and Writing and Mathematics, each scored between 200 and 800 points. The Reading and Writing section is
                                            split into two parts: the Reading section, which includes 52 questions based on passages
                                            that test reading comprehension and vocabulary in context, and the Writing and Language section, with 44 questions focused on grammar, punctuation, and sentence structure. This
                                            section lasts for a total of 1 hour and 25 minutes.
                                        </p>

                                        <p class="mt-2">
                                            The Mathematics section is divided into two parts as well: the No Calculator section, which has 20 questions to test algebra and problem-solving skills in 25 minutes, and the
                                            Calculator section, with 38 questions in 55 minutes that cover more advanced math topics
                                            like algebra and data analysis. The total testing time for the SAT without the optional Essay is 3 hours. While the Essay was previously an optional section, it will be
                                            discontinued starting in 2024 for most students. The maximum score for the SAT is 1600,
                                            combining the scores from the Reading/Writing and Math sections.
                                        </p>
