                                        <p class="mt-2">It assesses a candidate's ability to analyze data and draw conclusions using logical reasoning. Below is an overview of how the Quantitative section of the GMAT is evaluated.</p>

                                        <ul class="test-ul-con">
                                            <li>Applicants will be assessed on their problem-solving abilities using algebraic and arithmetic concepts.</li>
                                            <li>Applicants benefit from the GMAT Focus Edition as it provides an on-screen calculator for the Quantitative section, eliminating the need to bring a separate one.</li>
                                            <li>Geometry questions have been removed from the GMAT Focus Edition exam pattern.</li>
                                        </ul>

                                    </div>

                                    <div class="mt-4">
                                        <h4 class="none">Verbal Reasoning :</h4>

                                        <p class="mt-2">This section assesses candidates' ability to understand reading comprehension and evaluate critical reasoning in standard English.</p>

                                        <ul class="test-ul-con">
                                            <li>The assessment evaluates an applicant's comprehension skills and ability to make inferences from written texts through two types of multiple-choice questions.</li>
                                            <li>The first type involves reading passages of up to 350 words, followed by questions that assess your ability to interpret the text, draw inferences, and identify logical connections between different parts of the content.</li>
                                            <li>The second type, Critical Reasoning questions, measures your ability to draw conclusions from short argumentative passages.</li>
                                        </ul>

                                    </div>

