                                    <div class="mt-4">
                                        <p>
                                            The TOEFL (Test of English as a Foreign Language) measures the English proficiency of non-native speakers in four areas: reading, listening, speaking, and writing. It is typically taken in the iBT (Internet-based Test) format, with a total test duration of about 3 hours. Accepted by over 11,000 institutions globally, TOEFL scores assess a candidate's ability to use English in academic settings. Results are available within 6–10 days.
                                        </p>

                                    </div>

