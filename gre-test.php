                                    <h3><span class="down">Exam Pattern</span></h3>

                                    <div class="mt-4">
                                        <p>
                                            The GRE exam consists of three main sections: <span class="f-500"> Analytical Writing </span>, <span class="f-500">Verbal Reasoning</span>, and <span class="f-500">Quantitative Reasoning</span> . The Analytical Writing section includes two tasks—an "Issue Task" and an "Argument Task"—and is scored on a scale of 0 to 6, in half-point increments. The Verbal Reasoning section evaluates reading comprehension, text completion, and sentence equivalence, with scores ranging from 130 to 170, in 1-point increments.
                                        </p>
                                        <p class="mt-3">
                                            The Quantitative Reasoning section tests your mathematical reasoning, problem-solving, and data interpretation skills, also scored between 130 and 170, in 1-point increments. The total score for the GRE (combining Verbal and Quantitative Reasoning) ranges from 260 to 340. Additionally, there may be an unscored research section included to test new questions, though it does not count toward your final score. The exam is computer-based, and the total test duration is about 3 hours and 45 minutes, including breaks.
                                        </p>
